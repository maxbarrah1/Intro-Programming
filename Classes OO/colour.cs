namespace Classes_OO
{
    public class colour
    {
        //attributes
        public string red;
        public string blue;
        public string green;

        public string colourname;

        //constructors
        public colour(string red, string blue, string green, string colourname)
        {
            this.red = red;
            this.blue = blue;
            this.green = green;
            this.colourname = colourname;
        }
    }
}